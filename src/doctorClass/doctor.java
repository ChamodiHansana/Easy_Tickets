package doctorClass;

public class doctor {
    private String id;
    private String name;
    private String s;
    private String h;
    private String date;
    private String time;
    private String venue;
	public doctor(String id, String name, String s, String h, String date, String time, String venue) {
		super();
		this.id = id;
		this.name = name;
		this.s = s;
		this.h = h;
		this.date = date;
		this.time = time;
		this.venue = venue;
	}
	public String getId() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	
	public String getS() {
		return s;
	}
	
	public String getH() {
		return h;
	}
	
	public String getDate() {
		return date;
	}
	
	public String getTime() {
		return time;
	}
	
	public String getVenue() {
		return venue;
	}
	
    
    
	
	
	}
	
	
	